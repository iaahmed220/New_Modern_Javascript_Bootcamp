// Variables & Let

let age = 72;
age // returns 72
let hens = 4;
let roosters = 2;
console.log(hens + roosters) // returns 6

// Unary Operators
let score = 0;
score = score + 10;
score //returns 10

score += 1;
score // returns 11 

score -= 100;
score // returns -89

score = 150;
let bonusMult = 3;
score *= bonusMult;
score // returns 450

let counter = 0;
counter++;
counter // returns 1

let sum = 4;
sum--;
sum // returns 3

